#########################   Keyword Short cuts    #############################
#### Try to increase the hang of it to increase the  productivity #############

# shift + Ctrl + P   ---> Command Pallete
# Ctrl + ,           ---> Settings
# Ctrl +             ---> Screen Zoom in
# Ctrl + S           ---> Save
# shift + Ctrl + E   ---> Sidebar focus

# Home               ---> Start of line
# End                ---> End of line
# ctrl + Home        ---> Start of the page
# ctrl + End         ---> End of the page
# shift + Right      ---> selection of contents to right side
# shift + Left       ---> selection of contents to left side
# shift + Alt+ up    ---> selection of more line
# shift + Alt+ down  ---> selection of more line
# ctrl + X           ---> cut the line  (No need for selection)
# ctrl + C           ---> copy the line (No need for selection)
# ctrl + V           ---> Paste the line (No need for selection)
# Alt + up           ---> Move a full line up
# Alt + Down         ---> Move a full line Down

# Shift + Ctrl+ k    ---> Delete a line
# Shift + Ctrl+ Enter---> Insert a line below
# Ctrl + /           ---> Add comment
# Ctrl + D           ---> Select Next match
# Ctrl + U           ---> De-Select Next match
# Ctrl +Shift+ o     ---> Go to Entity (Function ,var,etc)

